        public static void Install(byte[] destination)
        {
            ArgumentNullException.ThrowIfNull(destination);
            if (destination.Length < 0x1000)
            {
                throw new ArgumentException("C64 character ROM buffer must be at least 4 KiB.", nameof(destination));
            }

            Array.Clear(destination);
            InstallSet(destination, 0);
            InstallSet(destination, CharacterSetSize);
        }

        private static void InstallSet(byte[] destination, int baseOffset)
        {
            for (var code = 0; code < 128; code++)
            {
                var glyph = GetGlyphForScreenCode(code);
                CopyGlyph(destination, baseOffset + (code * CharacterHeight), glyph, invert: false);
                CopyGlyph(destination, baseOffset + ((code + 128) * CharacterHeight), glyph, invert: true);
            }
        }

        private static byte[] GetGlyphForScreenCode(int code)
        {
            var character = code switch
            {
                0 => '@',
                >= 1 and <= 26 => (char)('A' + code - 1),
                27 => '[',
                28 => '\\',
                29 => ']',
                30 => '^',
                31 => '_',
                >= 32 and <= 63 => (char)code,
                _ => ' '
            };

            return Glyphs.TryGetValue(character, out var glyph)
                ? glyph
                : Glyphs[' '];
        }

        private static void CopyGlyph(byte[] destination, int offset, byte[] glyph, bool invert)
        {
            for (var row = 0; row < CharacterHeight; row++)
            {
                destination[offset + row] = invert ? (byte)~glyph[row] : glyph[row];
            }
        }

        private static byte[] Rows(params byte[] rows)
        {
            return rows;
        }
