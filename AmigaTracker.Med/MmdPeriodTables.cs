    public static int NormalizeNoteIndex(int noteNumber, sbyte playTranspose, sbyte transpose)
    {
        if (noteNumber <= 0)
        {
            return 0;
        }

        var index = noteNumber + playTranspose + transpose - 1;
        while (index < 0)
        {
            index += 12;
        }

        if (index > 62)
        {
            index -= 12;
        }

        return Clamp(index, 0, 62);
    }

    public static int GetPeriod(int noteNumber, sbyte finetune, sbyte transpose, sbyte playTranspose, bool useExtendedLowTable = false)
    {
        if (noteNumber <= 0)
        {
            return 0;
        }

        return GetPeriodByIndex(NormalizeNoteIndex(noteNumber, playTranspose, transpose), finetune, useExtendedLowTable);
    }

    public static int GetPeriodByIndex(int noteIndex, sbyte finetune, bool useExtendedLowTable = false)
    {
        return GetBasePeriodByIndex(noteIndex, finetune, useExtendedLowTable);
    }

    private static int GetBasePeriodByIndex(int noteIndex, sbyte finetune, bool useExtendedLowTable)
    {
        var tableIndex = Clamp(finetune, MinFineTune, MaxFineTune) - MinFineTune;
        if (useExtendedLowTable)
        {
            if (noteIndex < ExtendedLowPeriodTables[tableIndex].Length)
            {
                return ExtendedLowPeriodTables[tableIndex][Clamp(noteIndex, 0, ExtendedLowPeriodTables[tableIndex].Length - 1)];
            }

            return PeriodTables[tableIndex][Clamp(noteIndex - ExtendedLowPeriodTables[tableIndex].Length, 0, PeriodTables[tableIndex].Length - 1)];
        }

        return PeriodTables[tableIndex][Clamp(noteIndex, 0, PeriodTables[tableIndex].Length - 1)];
    }

    public static double GetSampleStep(int period, int sampleRate)
    {
        if (period <= 0 || sampleRate <= 0)
        {
            return 0.0;
        }

        return MmdConstants.PalPaulaClock / period / sampleRate;
    }

    private static int Clamp(int value, int min, int max)
    {
        if (value < min)
        {
            return min;
        }

        if (value > max)
        {
            return max;
        }

        return value;
    }
