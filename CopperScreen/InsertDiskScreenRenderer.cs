			'd' => new byte[] { 0x01, 0x01, 0x0F, 0x11, 0x11, 0x11, 0x0F },
			'e' => new byte[] { 0x00, 0x00, 0x0E, 0x11, 0x1F, 0x10, 0x0E },
			'f' => new byte[] { 0x06, 0x08, 0x1E, 0x08, 0x08, 0x08, 0x08 },
